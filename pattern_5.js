for(let i=0; i<=6; i++){
    for(let j=0; j<=5; j++){
        if(i===0 || i===2 || i===4 || i===6 || (i===3 && j===1) || (i===3 && j===4) || j===0 || j===5){
            str += "* ";
        }else{
            str += "  ";
        }
    }
    str += "\n";
}

